function getSkin(id) {
  return SKINS.find((s) => s.id === id) || SKINS[0];
}

function roleImageFor(role, skinId) {
  const file = ROLE_FILE_MAP[role];
  if (!file) return '';
  const skin = getSkin(skinId);
  const base = (skin && skin.imageBase) ? skin.imageBase : buildSkinAssetUrl(DEFAULT_SKIN_ID, 'role-base');
  return `${base}/${file}.png`;
}

function buildVarString(skin) {
  if (!skin || !skin.vars) return '';
  return Object.entries(skin.vars).map(([k, v]) => `${k}:${v}`).join(';');
}

module.exports = { CDN, DEFAULT_SKIN_ID, SKINS, getSkin, roleImageFor, buildVarString, buildSkinAssetUrl };
