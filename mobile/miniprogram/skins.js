const CDN = 'https://www.awalon.top/mp-assets';
const DEFAULT_SKIN_ID = 'dark-gold';

function buildSkinAssetUrl(skinId, assetName) {
  if (skinId === DEFAULT_SKIN_ID) {
    const defaults = {
      'home-bg': `${CDN}/in-game-bg-optimized.jpg?v=2`,
      'in-game-bg': `${CDN}/in-game-bg-optimized.jpg?v=2`,
      table: `${CDN}/table.png?v=8`,
      'quest-success': `${CDN}/quest-success-420x300.png?v=5`,
      'quest-fail': `${CDN}/quest-failed-420x300.png?v=5`,
      'role-base': `${CDN}/role-split`,
    };
    return defaults[assetName] || '';
  }
  if (assetName === 'role-base') return `${CDN}/skins/${skinId}/role-split`;
  return `${CDN}/skins/${skinId}/${assetName}`;
}

